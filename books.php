A book is a medium for recording information in the form of writing and images, typically composed of pages that are bound together and protected by a cover.[1] The technical term for this physical arrangement is codex. A single sheet in a codex is a leaf, and each side of a leaf is a page. In the history of hand-held physical supports for extended written compositions or records, the scroll dominated the written world prior to the codex.
The book has dual restricted and unrestricted definitions. In the restricted sense, a book is a self-sufficient section or part of a longer composition. The usage reflects the fact that, in antiquity, long works had to be written on several scrolls, and each scroll had to be identified by the book it contained. An example includes Aristotle's Physics in which each section is called a book. In the unrestricted sense, a book is the compositional whole of which such sections, whether called books or chapters or parts, are parts.
The content in a physical book need not be a composition. Books can consist only of drawings, engravings, photographs, and other visual arts. Some physical books are made with pages thick and sturdy enough to support other physical objects, like a scrapbook or photograph album. Books may be distributed in electronic form as e-books and other formats.
Although in ordinary academic parlance a monograph is understood to be a specialist academic work, rather than a reference work on a single scholarly subject, in library and information science monograph denotes more broadly any non-serial publication complete in one volume (book) or a finite number of volumes (even a novel like Proust's seven-volume In Search of Lost Time), in contrast to serial publications like a magazine, journal, or newspaper.
